<h2>1. Ennen rekisteröintiä</h2>

<h3><a name="lomake"></a>1.1. Rekisteröintilomakkeen täyttäminen ja käsittely</h3>

<p>Vain VRL-tunnuksen rekisteröineet jäsenet voivat rekisteröidä hevosiaan virtuaalihevosrekisteriin. Rekisteri löytyy kohdasta Virtuaalihevoset > <a href="<?php echo base_url('virtuaalihevoset/rekisterointi')?>" title="Rekisteröi virtuaalihevonen">Hevosten rekisteröinti</a>.
</p>

<p>Hevoset lisätään automaattisesti hevosrekisteriin. Jos huomaat tehneesi virheen tai haluat perua rekisteröinnin, voit poistaa hevosen rekisteristä vuorokauden (24 h) kuluessa rekisteröinnistä hevosen omasta profiilista. Vuorokauden jälkeen pystyt muokkaamaan hevosen omistajatietoja, ruunaamaan orin, muuttamaan sen säkäkorkeutta ja väriä sekä lisäämään sille puuttuvia tietoja. Muiden tietojen muutokset onnistuvat ainoastaan <a href="<?php echo base_url('tuki')?>" title="Ota yhteyttä tukeen" target="_blank">tuen</a> kautta. </p>


<h3><a name="sivut"></a>1.2. Hevosen sivut</h3>

<p>Hevosella tulee olla oma verkkosivu, josta löytyvät selkeästi suomen kielellä näissä ohjeissa mainitut pakolliset tiedot (nimi, rotu, sukupuoli, syntymäaika, omistaja) vaaditulla tavalla. Lisäksi hevosen sivuilla tulee olla maininta virtuaalisuudesta! Lue kohta: <a href="#teksti">Virtuaalihevonen-merkintä</a>.</p>

<p>Mikäli hevosen tiedot löytyvät joukkosivulta, tulee osoitteen viedä suoraan hevosen kohdalle (ankkurilinkki).</p>

<p>Mikäli hevosen sivut on salasanasuojattu, tulee käyttäjätunnuksen ja salasanan olla: user (tunnus) ja arabians (salasana). Jos nämä eivät toimi, hevosen sivut poistetaan rekisteristä ilman erillistä varoitusta.</p>

<p><b>Huomaathan, että rekisterissä ja hevosen sivulla ilmoitettujen tietojen on täsmättävä täydellisesti.</b></p>

<p>Sivulta ei saa löytyä laitonta materiaalia ja tekijänoikeudet tulee olla merkitty asianmukaisesti.</p>

<h3><a name="teksti"></a>1.3. Virtuaalihevonen-merkintä</h3>

<p>Hevosen sivulta tulee löytyä CTRL + F -selainhaulla <strong>näkyvä, kirjoitettu teksti “virtuaalihevonen”</strong> juuri tässä muodossa, esimerkiksi: "tämä on virtuaalihevonen". Merkintä otsikkokuvassa, muussa kuvassa, titlessä (sivun otsikossa) tai muu muoto, kuten virtuaaliponi/virtuaalinen hevonen, ei kelpaa.</p>

<p>Merkinnällä pyritään estämään virtuaalihevosten sekoittaminen oikeisiin hevosiin ja mahdollistamaan virtuaalihevosten poistaminen hakukoneiden hakutuloksista käyttämällä hakusanaa "virtuaalihevonen".</p>


<h2>2. Pakolliset tiedot</h2>

<p>Pakollisten tietojen tulee löytyä hevosen sivulta selkeästi merkittynä.</p>

<h3><a name="nimi"></a>2.1. Nimi</h3>
<p>Nimen tulee olla asiallinen ja hyvän maun mukainen. Rekisteristä voidaan myöhemmin sensuroida nimiä, joissa on kirosanoja tai selkeästi huumeisiin, väkivaltaan, rasismiin, seksiin tai muuhun mauttomaan tai sopimattomaan materiaaliin viittaavia sanoja. Hevosen nimellä ei saa myöskään olla selkeitä yhteyksiä tosielämään, eli se ei saa olla sama kuin jollain tunnetulla hevosella.</p>
<p>Erikoismerkit, kuten aksenttimerkit tai kyrilliset aakkoset, ovat nimissä sallittuja.</p>
<p><strong>Rekisteriin ei hyväksytä kahta täsmälleen samannimistä ja saman rotuista hevosta</strong>. Kasvattajaliite tekee nimestä eri nimen, esimerkiksi gotlanninrussorit Dahlgårds Joakim ja Karkurannan Joakim ovat kaksi eri nimeä, samoin kuin Joakim ja Joakim II.</p>

<h3><a name="rotu"></a>2.2. Rotu ja sukupuoli</h3>
<p>Hevosen sivulta tulee löytyä maininta hevosen rodusta ja sukupuolesta. Hevosen tulee täyttää ilmoitetun rodun rotumääritelmä sekä yksinkertaiset polveutumisehdot (suomenhevonen x puoliverinen ei voi olla suomenhevonen jne). Veriprosentteihin perustuvia polveutumisehtoja ei tarkisteta rekisterissä, mutta hevoselle on mahdollista anoa rodusta vastaavalta rotuyhdistykseltä polveutumistarkistusta, jos yhdistys on toiminnassa.</p>

<p>Hevosen rotua tai sukupuolta ei voi itse vaihtaa jälkikäteen (pois lukien orien ruunaus).</p>

<h3><a name="syntymaaika"></a>2.3. Syntymäaika</h3>
<p>Hevosen syntymäaika ilmoitetaan rekisteriin muodossa pp.kk.vvvv, esimerkiksi 01.10.2020. Rekisteriin ilmoitetun syntymäajan tulee täsmätä hevosen sivuilla ilmoitettuihin tietoihin, ja se tulee esittää sivuilla muodossa, josta ilmenee päivä, kuukausi ja vuosi, jolloin hevonen on syntynyt. <strong>Pelkkä syntymävuosi tai CAS-vuosi hevosen sivuilla ei riitä hevosen rekisteröintiin</strong>, vaan tiedossa tulee olla tarkka syntymäaika. </p> 
<p>Et voi rekisteröidä hevosia, jotka eivät ole vielä syntyneet!</p>

<h3><a name="suku"></a>2.4. Isä ja emä (suvulliset)</h3>
<p>Tarkista ennen rekisteröintiä, onko hevosen vanhemmat rekisteröity! <strong>Lomakkeelle täytetään vain vanhempien VH-rekisterinumerot</strong>.</p>
<p>Hevoselle voi lisätä vanhemmat myös jälkikäteen hevosen rekisteriprofiilista.</p>
<p><strong>Suvuttomien hevosten vanhempia ei ilmoiteta!</strong></p>

<h3><a name="omistaja"></a>2.5. Omistaja</h3>
<p>Hevosen sivulla tulee olla merkittynä hevosen omistajan VRL-tunnus. Voit rekisteröidä vain omia hevosiasi. Rekisteröinnin jälkeen hevoselle on mahdollista merkitä useita omistajia.</p>

<h3><a name="kasvattaja"></a>2.5. Kasvattaja ja kasvattajaliite (suvulliset)</h3>
<p>Jos hevosella on kasvattaja virtuaalimaailmassa, eli hevonen on syntynyt jo olemassa olevista virtuaalihevosista, kasvattajan tiedot (VRL-tunnus tai tallin rekisteritunnus) tulee merkitä rekisteröintilomakkeelle, mikäli kasvattaja ja/tai talli on rekisteröity VRL:oon. Tarkista kasvattajalta vaadittu merkintä! Hevosen sivulle tunnuksia ei tarvitse merkitä, ellei kasvattaja toisin vaadi.</p>
<p>Suvullisen hevosen nimessä oleva kasvattajaliite tulee merkitä erikseen lomakkeelle "Kasvattajaliite"-kenttään, vaikka kasvattajanimeä ei olisi vielä rekisteröity. </p>

<h2>3. Vapaaehtoiset tiedot</h2>

<h3><a name="vari"></a>3.1. Väri ja säkäkorkeus</h3>
<p>Jos hevosen väri ja säkäkorkeus on ilmoitettu rekisteriin, tulee näiden vastata hevosen sivulla olevia tietoja. Suosittelemme rodun rotumääritelmään tutustumista ennen hevosen rekisteröintiä. <strong>Ilmoita hevosen säkäkorkeus aikuisena</strong>.</p>
<p>Mikäli hevosen rekisteriprofiilissa esiintyy räikeitä epärealistisuuksia, kuten esimerkiksi säkäkorkeudeltaan 150 cm shetlanninponi tai suomenhevonen, jonka vanhemmat ovat arabianhevonen ja suomenhevonen, otetaan hevosen rekisteriin lisänneeseen henkilöön yhteyttä, jotta virheelliset tiedot saadaan oikaistua.</i></p>
<p>Jos huomaat itse hevosesi tiedoissa virheen, ota yhteys <a href="<?php echo base_url('tuki')?>" title="Ota yhteyttä tukeen" target="_blank">tukeen</a>.</p>

<h3><a name="ikaantyminen"></a>3.2. Ikääntyminen</h3>
<p>Ikääntyminen on pakollinen vain porrastetuissa kilpailuissa kilpailevilla hevosilla. Voit ilmoittaa kaikki tai osan syntymäpäivistä myös rekisteröinnin jälkeen. Täytä syntymäpäivät rekisteröintilomakkeelle muodossa pp.kk.vvvv, esimerkiksi 01.10.2021.</p>
<p>Ikääntymistä ei tarvitse olla hevosen sivulla näkyvillä, mutta jos se mainitaan rekisterissä ja hevosen sivulla, tietojen tulee olla keskenään yhtenevät.</p>

<h3><a name="sukuSuvuttomat"></a>3.3. Isä ja emä (suvuttomat)</h3>
<p><strong>Suvuttoman hevosen vanhempia ei merkitä rekisteriin</strong>.</p>

<h3><a name="kasvattajaSuvuttomat"></a>3.4. Kasvattajaliite ja kasvattaja (suvuttomat)</h3>
<p><strong>Suvuttoman hevosen kasvattajaa tai mahdollista keksittyä kasvattajaliitettä ei merkitä rekisteriin</strong>.

</p><h3><a name="painotus"></a>3.5. Painotus</h3>
<p>Voit halutessasi ilmoittaa hevosen painotuksen. Painotusta voi vaihtaa myös rekisteröinnin jälkeen.</p>

<h3><a name="kotitalli"></a>3.6. Kotitalli</h3>
<p>Voit halutessasi ilmoittaa hevosen kotitallin. Kotitallia voi vaihtaa myös rekisteröinnin jälkeen.</p>
<p>Täytä lomakkeelle tallin kahdeksanmerkkinen (8) tunniste, muodossa XXXX0000, <strong>älä tallin nimeä tai osoitetta!</strong></p>

<h3><a name="kuollut"></a>3.7. Kuollut</h3>
<p>Voit rekisteröidä myös kuolleita virtuaalihevosia tai merkata hevosen kuolinpäivän rekisteriin. Myös hevosen sivuilla täytyy olla selkeä ja näkyvä merkintä siitä, että hevonen on kuollut.</p>

